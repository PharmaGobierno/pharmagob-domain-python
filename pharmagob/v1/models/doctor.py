class Level(str, Enum):
    JUNIOR = "JUNIOR"
    SPECIALIST = "SPECIALIST"
    RESIDENT = "RESIDENT"
    SENIOR = "SENIOR"


class Job_Position(str, Enum):
    RESIDENT = "RESIDENT"


@dataclass(kw_only=True)
class DoctorModel(UpdatableModel):
    __entity_name__ = "doctors"

    umu_id: str
    name: str
    last_name_1: str
    last_name_2: Optional[str] = None
    employee_number: str
    profesional_licence: str
    specialty: str
    service: list[str]
    status: Status = Status.ACTIVE
    level: Optional[str] = None
    job_position: Optional[str] = None

    full_name: str = field(init=False)

    def __post_init__(self):
        super().__post_init__()
        self._id = uuid_by_params(self.employee_number, self.umu_id)
        self.full_name = (
            f"{self.name} {self.last_name_1} {self.last_name_2 or ''}".strip()
        )
